public class ContohArray {
	public static void main(String[] args) {
		// 1. Deklarasi array dengan 5 elemen
		int[] angka = new int[5];

		// 2. Memberi nilai ke setiap elemen array
		angka[0] = 10;
		angka[1] = 20;
		angka[2] = 30;
		angka[3] = 40;
		angka[4] = 50;

		// 3. Mengakses dan menampilkan elemen array
		System.out.println("Elemen ke-1 = " + angka[0]);
		System.out.println("Elemen ke-2 = " + angka[1]);
		System.out.println("Elemen ke-3 = " + angka[2]);
		System.out.println("Elemen ke-4 = " + angka[3]);
		System.out.println("Elemen ke-5 = " + angka[4]);
	}
}
